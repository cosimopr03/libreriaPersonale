package persistenza;

/**
 * Criteri disponibili per l’ordinamento della lista di libri.
 */
public enum CriterioOrdinamento
{
    /**
     * Ordina i libri in base al nome dell’autore, in ordine alfabetico.
     */
    AUTORE,

    /**
     * Ordina i libri in base al titolo, in ordine alfabetico.
     */
    TITOLO
}
